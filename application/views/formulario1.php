<script>
    $(document).ready(function(){
        $("#formulario1").validetta({
            bubblePosition: 'bottom',
            bubbleGapTop: 10,
            bubbleGapLeft: -5,
            onValid:function(e){
                e.preventDefault();
                $.ajax({
                    method:"POST",
                    url:"<?php base_url(); ?>registroFormAjax.php",
                    data:$("#formulario1").serialize(),
                    cache:false,
                    success:function(respAX){
                        var tipoAlerts = new Array("red","green");
                        var AX = JSON.parse(respAX);
                        $.alert({
                            title:AX.title,
                            icon: AX.icon,
                            content:AX.msj,
                            type:tipoAlerts[AX.val],
                            onDestroy:function(){
                                if (AX.val == 1) {
                                    window.location.replace("<?php base_url()?>formulario2");
                                } else {
                                    $('#formRegistro')[0].reset();
                                }
                            }
                        });
                    }
                });
            }
        });
    });
</script>
